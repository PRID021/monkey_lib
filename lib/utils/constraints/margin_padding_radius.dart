part of 'app_constraints.dart';

/// This class is used to define the margin, padding, radius and border of the app.
///
/// Large style
/// ```dart
/// static const double marginLarge = 12;
/// static const double paddingLarge = 12;
/// static const double radiusLarge = 12;
/// static const double borderLarge = 1.5;
/// static const double innerPaddingVerticalLarge = 12;
/// static const double innerPaddingHorizontalLarge = 16;
/// ```
///
/// Medium style
/// ```dart
/// static const double marginMedium = 8;
/// static const double paddingMedium = 8;
/// static const double radiusMedium = 8;
/// static const double borderMedium = 1;
/// static const double innerPaddingVerticalMedium = 8;
/// static const double innerPaddingHorizontalMedium = 12;
/// ```
///
/// Small style
/// ```dart
/// static const double marginSmall = 4;
/// static const double paddingSmall = 4;
/// static const double radiusSmall = 4;
/// static const double borderSmall = 0.5;
/// static const double innerPaddingVerticalSmall = 4;
/// static const double innerPaddingHorizontalSmall = 8;
/// ```
///
/// Extra large style
/// ```dart
/// static const double marginExtraLarge = 16;
/// static const double paddingExtraLarge = 16;
/// static const double radiusExtraLarge = 16;
/// static const double borderExtraLarge = 2;
/// static const double innerPaddingVerticalExtraLarge = 16;
/// static const double innerPaddingHorizontalExtraLarge = 24;
/// ```
///

class MarginPaddingRadiusConstraints {
  /// Small style
  static const double marginSmall = 4;
  static const double paddingSmall = 4;
  static const double radiusSmall = 4;
  static const double borderSmall = 0.5;
  static const double innerPaddingVerticalSmall = 4;
  static const double innerPaddingHorizontalSmall = 8;

  /// Medium style
  static const double marginMedium = 8;
  static const double paddingMedium = 8;
  static const double radiusMedium = 8;
  static const double borderMedium = 1;
  static const double innerPaddingVerticalMedium = 8;
  static const double innerPaddingHorizontalMedium = 12;

  /// Large style
  static const double marginLarge = 12;
  static const double paddingLarge = 12;
  static const double radiusLarge = 12;
  static const double borderLarge = 1.5;
  static const double innerPaddingVerticalLarge = 12;
  static const double innerPaddingHorizontalLarge = 16;

  /// Extra large style
  static const double marginExtraLarge = 16;
  static const double paddingExtraLarge = 16;
  static const double radiusExtraLarge = 16;
  static const double borderExtraLarge = 2;
  static const double innerPaddingVerticalExtraLarge = 16;
  static const double innerPaddingHorizontalExtraLarge = 24;
}
