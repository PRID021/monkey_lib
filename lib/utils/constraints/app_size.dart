part of 'app_constraints.dart';

/// This class is used to set the size of the app
/// ```dart
/// s style
/// static const double sIconButtonSize = 20;
/// static const double sIconSize = 16;
///
/// m style
/// static const double mIconButtonSize = 24;
/// static const double mIconSize = 20;
///
/// l style
/// static const double lIconButtonSize = 32;
/// static const double lIconSize = 24;
///
/// Extra l style
/// static const double extraLIconButtonSize = 40;
/// static const double extraLIconSize = 28;
/// ```

class AppSize {
  /// s style
  static const double sIconButtonSize = 20;
  static const double sIconSize = 16;
  static const double sBorderWidth = 0.5;

  /// m style
  static const double mIconButtonSize = 24;
  static const double mIconSize = 20;
  static const double mBorderWidth = 1;

  /// l style
  static const double lIconButtonSize = 32;
  static const double lIconSize = 24;
  static const double lBorderWidth = 1.5;

  /// Extra l style
  static const double extraLIconButtonSize = 40;
  static const double extraLIconSize = 28;
  static const double extraLBorderWidth = 2;
}
